public class Area {
    public static void main(String args[]) {
        // Default value of double is 0.0d

        double r, pi, a;
        r = 10.8;
        pi = 3.14;
        a = pi * r * r;
        System.out.println("Area of circle is: " + a);

        /* Default value of float is 0.0f */

        float r1, pi1, a1;
        r1 = 10.8f;
        pi1 = 3.14f;
        a1 = pi1 * r1 * r1;
        System.out.println("Area of circle is: " + a1);
    }
}
